function tab1(a1,a2,a3,a4)
{
    var Show1a = document.querySelector('.cd-SPshowH1a');
    var Show1b = document.querySelector('.cd-SPshowH1b');
    var Show2a = document.querySelector('.cd-SPshowH2a');
    var Show2b = document.querySelector('.cd-SPshowH2b');
    var Show3a = document.querySelector('.cd-SPshowH3a');
    var Show3b = document.querySelector('.cd-SPshowH3b');
    var Show4a = document.querySelector('.cd-SPshowH4a');
    var Show4b = document.querySelector('.cd-SPshowH4b');
    
    if (document.getElementById(a1).className != 'chon'){
        document.getElementById(a1).classList.add('chon');   
        document.getElementById(a2).classList.remove('chon');
        document.getElementById(a3).classList.remove('chon');
        document.getElementById(a4).classList.remove('chon');
        
        Show1a.classList.add('SPshowRH1a');
        Show1b.classList.add('SPshowLH1b');
        Show1a.classList.remove('SPshowLH1a');
        Show1b.classList.remove('SPshowRH1b');


        Show2a.classList.remove('SPshowRH2a');
        Show2b.classList.remove('SPshowLH2b');
        Show2a.classList.add('SPshowLH2a');
        Show2b.classList.add('SPshowRH2b');

        Show3a.classList.remove('SPshowRH3a');
        Show3b.classList.remove('SPshowLH3b');
        Show3a.classList.add('SPshowLH3a');
        Show3b.classList.add('SPshowRH3b');

        Show4a.classList.remove('SPshowRH4a');
        Show4b.classList.remove('SPshowLH4b');
        Show4a.classList.add('SPshowLH4a');
        Show4b.classList.add('SPshowRH4b');
    }
}

